void select_column(int col)
{

	GPIOB->ODR |= ((1 << 8) | (1 << 7) | (1 << 6) | (1 << 5) | (1 << 4) | (1 << 3) | (1 << 1) | (1 << 0));

	col = 7 - col;

	if(col == 0)
	{
		GPIOB->ODR &= ~(1 << 0);
	}

	if(col == 1)
	{
		GPIOB->ODR &= ~(1 << 1);
	}

	if(col == 2)
	{
		GPIOB->ODR &= ~(1 << 3);
	}

	if(col == 3)
	{
		GPIOB->ODR &= ~(1 << 4);
	}

	if(col == 4)
	{
		GPIOB->ODR &= ~(1 << 5);
	}

	if(col == 5)
	{
		GPIOB->ODR &= ~(1 << 6);
	}

	if(col == 6)
	{
		GPIOB->ODR &= ~(1 << 7);
	}

	if(col == 7)
	{
		GPIOB->ODR &= ~(1 << 8);
	}

}

void set_data(int x)
{
	GPIOA->ODR = row[x];
}

